"""生成进度汇报 Word — 简洁段落式"""
import os
from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
from datetime import datetime

DESKTOP = os.path.expanduser("~/Desktop")
OUTPUT = os.path.join(DESKTOP, "通用记忆建模与多层记忆管理_进度汇报.docx")

FONT = '宋体'
SIZE = Pt(12)

doc = Document()

s = doc.styles['Normal']
s.font.name = FONT
s.font.size = SIZE
s.font.color.rgb = None
s.paragraph_format.space_before = Pt(0)
s.paragraph_format.space_after = Pt(4)
s.paragraph_format.line_spacing = 1.25
s.paragraph_format.first_line_indent = Cm(0.7)
s.element.rPr.rFonts.set(qn('w:eastAsia'), FONT)

for sec in doc.sections:
    sec.top_margin = Cm(2.0)
    sec.bottom_margin = Cm(2.0)
    sec.left_margin = Cm(2.5)
    sec.right_margin = Cm(2.5)


def rf(run, size=None, bold=False):
    run.font.name = FONT
    run.element.rPr.rFonts.set(qn('w:eastAsia'), FONT)
    run.font.size = size or SIZE
    run.font.color.rgb = None
    run.bold = bold


def heading(text, level=1):
    sz = {1: Pt(16), 2: Pt(14)}.get(level, Pt(14))
    h = doc.add_heading(text, level=level)
    h.paragraph_format.space_before = Pt(12)
    h.paragraph_format.space_after = Pt(4)
    h.paragraph_format.first_line_indent = Cm(0)
    for r in h.runs:
        rf(r, size=sz, bold=True)


def para(text):
    p = doc.add_paragraph()
    r = p.add_run(text)
    rf(r)


def para_no_indent(text):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(0)
    r = p.add_run(text)
    rf(r)


# ================================================================
# 封面
# ================================================================
t = doc.add_paragraph()
t.alignment = WD_ALIGN_PARAGRAPH.CENTER
t.paragraph_format.first_line_indent = Cm(0)
rf(t.add_run('通用记忆建模与多层记忆管理功能\n进度汇报'), size=Pt(20), bold=True)

sp = doc.add_paragraph()
sp.alignment = WD_ALIGN_PARAGRAPH.CENTER
sp.paragraph_format.first_line_indent = Cm(0)
rf(sp.add_run(f'{datetime.now().strftime("%Y年%m月%d日")}'), size=Pt(11))

